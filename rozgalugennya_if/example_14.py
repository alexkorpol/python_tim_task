# Для даного числа n < 100 необхідно закінчити фразу «На лузі пасеться…​» одним 
# з можливих продовжень: n корів, n корова, n корови, правильно обравши закінчення. 
# Вводиться натуральне число. Програма повинна вивести введене число n і одне зі слів: 
# koriv, korova або korovy. Між числом і словом повинен стояти один пропуск.

# Вхідні дані:

# 5
# 23
# 51
# Вихідні дані:

# 5 koriv
# 23 korovy
# 51 korova

#*============Code================

# Зчитуємо натуральне число n з клавіатури
n = int(input())

# Визначаємо правильне закінчення для фрази
if 10 <= n % 100 <= 20 or n % 10 == 0 or 5 <= n % 10 <= 9:
    ending = "koriv"
elif n % 10 == 1:
    ending = "korova"
else:
    ending = "korovy"

# Виводимо результат
print(f"{n} {ending}")