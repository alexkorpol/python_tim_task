# Виведіть повідомлення Hello, Python! на екран n разів 
# (n - ціле число, яке вводить користувач).

# Вхідні дані:

# 3
# Вихідні дані:

# Hello, Python!
# Hello, Python!
# Hello, Python!

#*============Code================

# Зчитуємо ціле число n з клавіатури
n = int(input("Введіть ціле число n: "))

# Виводимо повідомлення n разів
for _ in range(n):
    print("Hello, Python!")